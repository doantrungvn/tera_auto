package org.terasoluna.qp.domain.model;

public class ScreenFormTabs {
	private Long screenFormTabId;
	private String tabTitle;
	private String areas;
	private Long screenFormId;
	private String tabCode;
	private Integer tabDirection;
	
	public Long getScreenFormTabId() {
		return screenFormTabId;
	}
	public void setScreenFormTabId(Long screenFormTabId) {
		this.screenFormTabId = screenFormTabId;
	}
	public String getTabTitle() {
		return tabTitle;
	}
	public void setTabTitle(String tabTitle) {
		this.tabTitle = tabTitle;
	}
	public String getAreas() {
		return areas;
	}
	public void setAreas(String areas) {
		this.areas = areas;
	}
	public Long getScreenFormId() {
		return screenFormId;
	}
	public void setScreenFormId(Long screenFormId) {
		this.screenFormId = screenFormId;
	}
	public String getTabCode() {
		return tabCode;
	}
	public void setTabCode(String tabCode) {
		this.tabCode = tabCode;
	}
	public Integer getTabDirection() {
		return tabDirection;
	}
	public void setTabDirection(Integer tabDirection) {
		this.tabDirection = tabDirection;
	}


	
}
